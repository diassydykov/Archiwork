/** Normalized room rectangle (0..1), south-west origin */

export interface UnitRoom {
  id: string;
  name: string;
  x: number;
  y: number;
  w: number;
  h: number;
}

export interface FloorArchetype {
  id: string;
  label: string;
  rooms: UnitRoom[];
  /** Wet core for vertical stacking between floors */
  wetCore?: { x: number; y: number; w: number; h: number };
  stairs?: { x: number; y: number; w: number; h: number };
}

function r(
  id: string,
  name: string,
  x: number,
  y: number,
  w: number,
  h: number
): UnitRoom {
  return { id, name, x, y, w, h };
}

